package exemplocalculadora;

public class Calculadora {
	 // método
	 public int adicao(int a, int b){
		 int r;
		 r = a + b;
		 return r;
	 }
	 
	 public int sub(int a, int b){
		 int r;
		 r = a - b;
		 return r;
	 }
	 
	 public int mult(int a, int b){
		 int r;
		 r = a * b;
		 return r;
	 }
	 
	 public int div(int a, int b){
		 int r;
		 r = a / b;
		 return r;
	 }
}
